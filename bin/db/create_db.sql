------------------------------------TABLA EMPRESA------------------------------------

DROP TABLE EMPRESA CASCADE CONSTRAINT;

CREATE TABLE EMPRESA(
    CIF VARCHAR(10) NOT NULL,
    NOMBRE VARCHAR(12) NOT NULL,
    PAIS VARCHAR(20) NOT NULL,
    DIRECCION VARCHAR(30) NOT NULL,
    TEL VARCHAR(12) NOT NULL,
    EMAIL VARCHAR(30) NOT NULL,
    CONSTRAINT PK_EMPRESA1 PRIMARY KEY (CIF)
);

------------------------------------TABLA GASTOS------------------------------------

DROP TABLE GASTOS CASCADE CONSTRAINT;

CREATE TABLE GASTOS(
    NUM_FACTURA VARCHAR(10),
    CIF VARCHAR(10) NOT NULL,
    TIPO VARCHAR(12) NOT NULL,
    FECHA_GAST DATE NOT NULL,
    DESCRIPCION VARCHAR(30) NOT NULL,
    PRECIO NUMERIC NOT NULL ,
    CONSTRAINT PK_GASTOS PRIMARY KEY (NUM_FACTURA),
    CONSTRAINT FK_EMPRESA1 FOREIGN KEY (CIF) REFERENCES EMPRESA(CIF)
);

------------------------------------TABLA INFORME------------------------------------

DROP TABLE INFORME CASCADE CONSTRAINT;

CREATE TABLE IVA(
    NUM_INFORME VARCHAR(10),
    CIF VARCHAR(10) NOT NULL,
    FECHA_INFO DATE NOT NULL,
    CONSTRAINT PK_INFORME PRIMARY KEY (NUM_INFORME),
    CONSTRAINT FK_EMPRESA1 FOREIGN KEY (CIF) REFERENCES EMPRESA(CIF)
);